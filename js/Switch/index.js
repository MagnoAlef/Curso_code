const resultado = prompt("Escolha uma alternativa : [a][b][c]")
switch (resultado) {
  case "a": // case vem de caso 
    alert('O resultado é "a"');
    break
  case "b":
    alert('O resultado é "b"');
    break
  case "c":
    alert('O resultado é "c"');
    break
  default:
    alert('Finalizando')
}
